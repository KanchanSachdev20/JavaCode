package OOP_Inheritance;

public class Car extends Vehicle {

	public void start() {
		System.out.println("start car");
	}

	public void stop() {
		System.out.println("stop car");

	}

	public void refuel() {
		System.out.println("refuel car");
	}
	
	public void petrolEngine() {
		System.out.println("car--petrol engine");
	}
}
