package Assignments;

public class LoopAssignment {

	public static void main(String[] args) {
		
		for(int i=1 ; i<=5; i++) {
			System.out.println("I Am Batman");
		}
		

		for(int k=10 ; k>=1; k--)
		{
			System.out.println(k);
		}
		
		int n =1;
		for(;n<=100;n++) {
			if(n%5==0) {
			System.out.println("Multiplication of 5" + n);
		}
		}
	}

}
