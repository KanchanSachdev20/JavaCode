package JavaSessions;

public class DataTypesConcept {

	public static void main(String[] args) {
		//byte
		
		byte b = 10;
		byte b1 = 20;
		b1=30;
		byte b2 = 40;
		System.out.println(b+b1);
		
		//short
		
		short s = 128;
		System.out.println(s);
		
		//int
		
		int i = 123456;
		int i1 = 123456789;
		System.out.print(i+i1);

		
		//long
		long l = 545328788888l;
		System.out.println(l);
		
		//char
		char c ='a';
		char c1 = '#';
		System.out.println(c1);
		
		//float
		float f= 100;
		float f1= 200.80f;
		float f2 =(float)500.34;
		System.out.println(f);
		System.out.print(f2);
		
		//double
		double d = 788.37899;
		System.out.print(d);
		
		
	}
	
	

}
